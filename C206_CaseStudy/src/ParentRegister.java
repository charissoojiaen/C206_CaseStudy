
public class ParentRegister {
	
		private String studentId;
		private String regiId;
		public ParentRegister(String studentId, String regiId) {
		this.studentId = studentId;
		this.regiId = regiId;
		}
		
		public String getStudentId() {
		return studentId;
		}
		public String getRegiId() {
		return regiId;
		}

}
